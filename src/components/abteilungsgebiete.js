
function calculateCenter(coordinates) {

  let sumX = 0;
  let sumY = 0;
  const numCoordinates = coordinates.length;

  coordinates.forEach(coord => {
      sumX += coord[0];
      sumY += coord[1];
  });

  const centerX = sumX / numCoordinates;
  const centerY = sumY / numCoordinates;
  return { x: centerX, y: centerY };
}

export const centerOfAbteilungsgebiet = (abteilungsgebiet) => {
  return calculateCenter(abteilungsgebiet.geometry.coordinates[0]);
}

